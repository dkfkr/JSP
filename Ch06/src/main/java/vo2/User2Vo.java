package vo2;

public class User2Vo {
	
	private String uid;
	private String name;
	private String hp;
	private String age;
	
	public String getUid() {
		return uid;
	}
	
	public String getName() {
		return name;
	}
	
	public String getHp() {
		return hp;
	}
	
	public String getAge() {
		return age;
	}
	
	public void setUid(String uid) {
		this.uid = uid;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public void setHp(String hp) {
		this.hp = hp;
	}
	
	public void setAge(String age) {
		this.age = age;
	}
}
